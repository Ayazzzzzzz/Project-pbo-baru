    //tombol pesan
    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        
        //error handling apabila belum login
        if ("0".equals(id.getText()) || id.getText() == null) {
            JOptionPane.showMessageDialog(null, "Anda Belum Login");
            return;
        }
        
        //error handlling jika belum memilih film
        if ("0".equals(idFilm.getText()) || idFilm.getText() == null
                || "Judul".equals(judul.getText())
                || "Genre".equals(genre.getText())
                || "Tahun".equals(tahun1.getText())
                || "Durasi".equals(durasi.getText())) {
            JOptionPane.showMessageDialog(null, "Film Belum DIpilih");
            return;
        }

        filmId = Integer.parseInt(getIdFilm().getText());
        
        //ke frame pesantiket
        pesantiket v = new pesantiket(idPegawai, filmId);
        v.setVisible(true);
        v.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_pesanActionPerformed

