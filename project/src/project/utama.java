    //jika user meng-klik tabel film
    private void tabelFilmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelFilmMouseClicked

        //mengambil data dari baris yang dipilih
        int row = tabelFilm.getSelectedRow();
        idFilm.setText(tabelFilm.getValueAt(row, 0).toString());
        judul.setText(tabelFilm.getValueAt(row, 1).toString());
        genre.setText(tabelFilm.getValueAt(row, 2).toString());
        tahun1.setText(tabelFilm.getValueAt(row, 3).toString());
        durasi.setText(tabelFilm.getValueAt(row, 4).toString());
        
        //memunculkan gambar dari url
        String urlGambar = tabelFilm.getValueAt(row, 5).toString();
        try {
            java.net.URL url = new java.net.URL(urlGambar);
            BufferedImage img = ImageIO.read(url);

            if (img != null) {
                //supaya gambar fit di jLabel
                Image scaledImg = img.getScaledInstance(gambar.getWidth(), gambar.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(scaledImg);
                gambar.setIcon(imageIcon);
            } else {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memuat gambar!");
            }
        } catch (MalformedURLException e) {
            System.err.println("Kesalahan pada URL: " + e.getMessage());
        } catch (IOException e) {
            System.err.println("IOException ketika memuat gambar: " + e.getMessage());
        }
    }//GEN-LAST:event_tabelFilmMouseClicked

