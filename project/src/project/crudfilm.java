    //tombol insert
    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed

        try {

            //jika ada field yang kosong
            if (judul.getText().isEmpty() || genre.getText().isEmpty() || tahun1.getText().isEmpty() || durasi.getText().isEmpty() || poster.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Lengkapi data film yang ingin ditambahkan!");
                return;
            }

            //validasi format tahun (YYYY)
            if (!tahun1.getText().matches("\\d{4}")) {
                JOptionPane.showMessageDialog(null, "Format Tahun tidak sesuai! (YYYY)");
                return;
            }

            //validasi format durasi (x jam x menit)
            if (!durasi.getText().matches("\\d+ jam \\d+ menit")) {
                JOptionPane.showMessageDialog(null, "Format Durasi tidak sesuai! (n jam m menit)");
                return;
            }

            //validasi format poster (URL)
            if (!poster.getText().matches("https?://.+")) {
                JOptionPane.showMessageDialog(null, "Format poster harus berupa URL yang valid!");
                return;
            }

            f.insert();
            f.isitabel2();
            JOptionPane.showMessageDialog(null, "Data film berhasil ditambahkan!");

            //clear text field
            clear();
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_insertActionPerformed

