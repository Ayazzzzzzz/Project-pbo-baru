    //tombol hapus
    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed

        try {
            int row = tabelFilm.getSelectedRow();
            //jika belum pilih film
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Pilih film yang ingin dihapus!");
                return;
            }

            String judulP = judul.getText();

            //cek ulang
            int response = JOptionPane.showConfirmDialog(null,
                    "Yakin ingin menghapus film " + judulP + "?",
                    "Konfirmasi Penghapusan",
                    JOptionPane.YES_NO_OPTION);

            if (response == JOptionPane.YES_OPTION) {
                //kode yang dijalankan jika user memilih Yes
                JOptionPane.showMessageDialog(null, "Film berhasil dihapus!");
            } else if (response == JOptionPane.NO_OPTION) {
                //kode yang dijalankan jika user memilih No
                JOptionPane.showMessageDialog(null, "Penghapusan film dibatalkan!");
            }

            f.delete();
            f.isitabel2();
            JOptionPane.showMessageDialog(null, "Data film berhasil dihapus!");

            //clear text field
            clear();
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_deleteActionPerformed

